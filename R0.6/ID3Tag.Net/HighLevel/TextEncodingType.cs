namespace ID3Tag.HighLevel
{
    /// <summary>
    /// Defines the Text Encoding.
    /// </summary>
    public enum TextEncodingType
    {
        /// <summary>
        /// ISO-8859-1 coding.
        /// </summary>
        Ansi = 0,
        /// <summary>
        /// Unicode UTF16 coding.
        /// </summary>
        UTF16 = 1,
        /// <summary>
        /// Unicode UTF16 Big Endian coding.
        /// </summary>
        UTF16_BE = 2,
        /// <summary>
        /// Unicode UTF8 coding.
        /// </summary>
        UTF8 = 3
    }
}